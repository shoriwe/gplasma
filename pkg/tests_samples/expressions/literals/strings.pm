# Single Quote Strings
println('Hello World')
println('Hello
World')
# Double Quote Strings
println("Hello World")
println("Hello
World")
# Byte Strings
println(b"Hello world")
# Escaped chars
println("Hello\nWorld")
println("Hello\x41World")
println("\u0041ntonio")
println("Hello\\x41World")
println("500\u20ac")
println("500\\u20ac")